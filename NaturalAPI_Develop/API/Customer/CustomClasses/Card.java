public class Card {
	
	private int number;

	public void setnumber(int number) {
		this.number = number;
	}

	public int getnumber() {
		return number;
	}

	
	private Cash available;

	public void setavailable(Cash available) {
		this.available = available;
	}

	public Cash getavailable() {
		return available;
	}

}